        rangeAxis.setTickLabelPaint(Color.GRAY);
        rangeAxis.setUpperMargin(0.3);
        rangeAxis.setTickLabelsVisible(false);
        rangeAxis.setTickMarksVisible(false);
        rangeAxis.setVisible(false);
        rangeAxis.setAxisLineVisible(false);

